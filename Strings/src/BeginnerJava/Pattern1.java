package BeginnerJava;

public class Pattern1 {
	public static void main(String[] args) {
	
			for (int i=1;i<=5;i++)
			{
				for (int j1=1;j1<=i;j1++)
				{
					System.out.print("-");
				}

			
				for (int j1=1;j1<=i;j1++)
				{
					System.out.print("*");
				}
				System.out.println();
			}
			}
		
	}
	

